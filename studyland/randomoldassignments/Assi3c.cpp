#include <iostream>
using namespace std;

int main()
{
  char choice = 'c';

  switch (choice) {
    case 'a':
    cout << "a" << endl;

    case 'b':
    cout << "b" << endl;

    case 'c':
    cout << "c" << endl;

    case 'd':
    cout << "d" <<endl;

    default:
    cout << "default" <<endl;
  }

  return 0;
}
